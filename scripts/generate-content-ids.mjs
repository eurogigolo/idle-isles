import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname } from 'node:path'

const registryPath = 'content/core/ids.json'
const outputPath = 'src/generated/contentIds.ts'
const checkMode = process.argv.includes('--check')

const registry = JSON.parse(await readFile(registryPath, 'utf8'))
const output = generateContentIds(registry)

if (checkMode) {
  let current = ''

  try {
    current = await readFile(outputPath, 'utf8')
  } catch {
    // Missing generated output is reported by the drift check below.
  }

  if (normalizeLineEndings(current) !== normalizeLineEndings(output)) {
    console.error(`${outputPath} is out of date. Run npm run content:generate.`)
    process.exitCode = 1
  } else {
    console.log(`${outputPath} is up to date.`)
  }
} else {
  await mkdir(dirname(outputPath), { recursive: true })
  await writeFile(outputPath, output, 'utf8')
  console.log(`Generated ${outputPath}`)
}

function generateContentIds(data) {
  return `${banner()}
import type { ActivityId, AreaId, ItemId } from '../game'

export type ContractStartKind = 'combat' | 'gather' | 'artisan'
export type ContractActivityIds = Partial<
  Record<ActivityId, { id: number; kind: ContractStartKind }>
>

export const CONTRACT_AREA_IDS = {
${formatAreaEntries(data.areas)}
} satisfies Record<AreaId, number>

export const CONTRACT_ITEM_IDS = {
${formatItemEntries(data.items)}
} satisfies Record<ItemId, bigint>

export const CONTRACT_ACTIVITY_IDS: ContractActivityIds = {
${formatActivityEntries(data.activities)}
}
`
}

function banner() {
  return '// This file is generated by scripts/generate-content-ids.mjs. Do not edit by hand.'
}

function formatAreaEntries(entries) {
  return entries
    .map((entry) => `  ${formatKey(entry.localId)}: ${entry.chainId},`)
    .join('\n')
}

function formatItemEntries(entries) {
  return entries
    .map((entry) => `  ${formatKey(entry.localId)}: ${entry.chainId}n,`)
    .join('\n')
}

function formatActivityEntries(entries) {
  return entries
    .map(
      (entry) =>
        `  ${formatKey(entry.localId)}: { id: ${entry.chainId}, kind: '${entry.kind}' },`,
    )
    .join('\n')
}

function formatKey(value) {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(value) ? value : JSON.stringify(value)
}

function normalizeLineEndings(value) {
  return value.replace(/\r\n/g, '\n')
}
